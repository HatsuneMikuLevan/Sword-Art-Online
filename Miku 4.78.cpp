#include
int main() {
int a, b, c, d;
std::cout << "Введите координаты первого поля (a, b): ";
std::cin >> a >> b;
std::cout << "Введите координаты второго поля (c, d): ";
std::cin >> c >> d;

// Проверяем, что координаты находятся в диапазоне от 1 до 8
if (a < 1 || a > 8 || b < 1 || b > 8 || c < 1 || c > 8 || d < 1 || d > 8) {
std::cout << "Координаты должны быть в диапазоне от 1 до 8." << std::endl;
} else {
// Проверяем, совпадают ли цвета полей
if ((a + b) % 2 == (c + d) % 2) {
std::cout << "Поля (" << a << ", " << b << ") и (" << c << ", " << d << ") одного цвета." << std::endl;
} else {
std::cout << "Поля (" << a << ", " << b << ") и (" << c << ", " << d << ") разного цвета." << std::endl;
}
}

return 0;
}
