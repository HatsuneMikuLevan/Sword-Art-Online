#include
int main() {
int a1, a2, b1, b2;
std::cout << "Введите цифры первого двузначного числа (десятки, единицы): ";
std::cin >> a2 >> a1;
std::cout << "Введите цифры второго двузначного числа (десятки, единицы): ";
std::cin >> b2 >> b1;

int A = a2 * 10 + a1;
int B = b2 * 10 + b1;
int C = A - B;

int c2 = C / 10;
int c1 = C % 10;

std::cout << "Цифры разности: " << c2 << " (десятки), " << c1 << " (единицы)" << std::endl;

return 0;
}
