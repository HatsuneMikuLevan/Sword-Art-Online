#include
int main() {
int n;
std::cout << "Введите двузначное число: ";
std::cin >> n;

int digit1 = n / 10;
int digit2 = n % 10;

// Проверка на цифру 3
bool has_3 = (digit1 == 3 || digit2 == 3);
std::cout << "а) Входит ли в него цифра 3? " << (has_3 ? "Да" : "Нет") << std::endl;

// Проверка на цифру а (в задаче опечатка, должно быть число)
// Предположим, что "а" - это переменная, которую введет пользователь.
int a;
std::cout << "Введите цифру а для проверки: ";
std::cin >> a;
bool has_a = (digit1 == a || digit2 == a);
std::cout << "б) Входит ли в него цифра " << a << "? " << (has_a ? "Да" : "Нет") << std::endl;

return 0;
}
