#include <iostream>

int main() {
    int n, a;
    std::cout << "Введите четырехзначное число: ";
    std::cin >> n;
    std::cout << "Введите число а для проверки пункта г): ";
    std::cin >> a;

    int d1 = n / 1000;
    int d2 = (n / 100) % 10;
    int d3 = (n / 10) % 10;
    int d4 = n % 10;

    // а) равна ли сумма двух первых его цифр сумме двух его последних цифр
    std::cout << "а) Сумма двух первых цифр равна сумме двух последних: " << ((d1 + d2) == (d3 + d4) ? "Да" : "Нет") << std::endl;

    // б) кратна ли трем сумма его цифр
    std::cout << "б) Сумма цифр кратна трем: " << ((d1 + d2 + d3 + d4) % 3 == 0 ? "Да" : "Нет") << std::endl;

    // в) кратно ли четырем произведение его цифр
    std::cout << "в) Произведение цифр кратно четырем: " << ((d1 * d2 * d3 * d4) % 4 == 0 ? "Да" : "Нет") << std::endl;

    // г) кратно ли произведение его цифр числу а
    std::cout << "г) Произведение цифр кратно числу а: " << ((d1 * d2 * d3 * d4) % a == 0 ? "Да" : "Нет") << std::endl;

    return 0;
}
