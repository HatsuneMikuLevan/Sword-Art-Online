

#include <iostream>
#include <vector>
#include <iomanip>

int main() {
    const int num_cars = 30;
    const double max_power = 80.0;
    // Пример данных
    std::vector<double> powers = {
        70.0, 90.0, 80.0, 110.0, 75.0, 120.0, 65.0, 85.0, 95.0, 70.0,
        100.0, 78.0, 82.0, 60.0, 130.0, 80.0, 70.0, 115.0, 90.0, 75.0,
        105.0, 68.0, 88.0, 92.0, 72.0, 125.0, 80.0, 70.0, 98.0, 74.0
    };
    std::vector<double> costs = {
        500000.0, 700000.0, 600000.0, 900000.0, 550000.0, 1000000.0, 450000.0, 650000.0, 750000.0, 520000.0,
        800000.0, 580000.0, 620000.0, 400000.0, 1100000.0, 610000.0, 510000.0, 950000.0, 710000.0, 560000.0,
        850000.0, 480000.0, 680000.0, 720000.0, 530000.0, 1050000.0, 630000.0, 540000.0, 780000.0, 545000.0
    };

    std::cout << std::fixed << std::setprecision(2);
    std::cout << "Стоимость автомобилей с мощностью двигателя не более 80 л. с.:\n";

    for (int i = 0; i < num_cars; ++i) {
        if (powers[i] <= max_power) {
            std::cout << "Автомобиль " << i + 1 << ": Мощность = " << powers[i] << " л. с., Стоимость = " << costs[i] << " руб.\n";
        }
    }

    return 0;
}
