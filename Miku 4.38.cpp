#include <iostream>
#include <algorithm>

int main() {
    int a, b, c, d;
    std::cout << "Введите размеры стола a и b (a > b): ";
    std::cin >> a >> b;
    std::cout << "Введите размеры прямоугольников c и d (c > d): ";
    std::cin >> c >> d;

    // Размещение длинной стороной вдоль длинной стороны стола
    int count1 = (a / c) * (b / d);

    // Размещение длинной стороной вдоль короткой стороны стола
    int count2 = (a / d) * (b / c);

    if (count1 > count2) {
        std::cout << "Больше прямоугольников поместится, если их длинная сторона будет вдоль длинной стороны стола. Количество: " << count1 << std::endl;
    } else if (count2 > count1) {
        std::cout << "Больше прямоугольников поместится, если их длинная сторона будет вдоль короткой стороны стола. Количество: " << count2 << std::endl;
    } else {
        std::cout << "Количество прямоугольников одинаково в обоих случаях. Количество: " << count1 << std::endl;
    }

    return 0;
}
